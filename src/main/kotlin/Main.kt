fun main(args: Array<String>) {
    var firstNumber = 8
    val secondNumber = 5
    println("First Number + Second  Number = ${firstNumber+secondNumber}")
    println("First Number - Second  Number = ${firstNumber-secondNumber}")
    println("First Number / Second  Number = ${firstNumber/secondNumber}")
    println("First Number % Second  Number = ${firstNumber%secondNumber}")
    println("First Number * Second  Number = ${firstNumber*secondNumber}")
}